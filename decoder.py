q_array = [0x75, 0x3d, 0x32, 0x38, 0x69, 0x72, 0x40, 0x3f, 0x38, 0x43, 0x32, 0x45, 0x44, 0x4a, 0x40, 0x46, 0x38, 0x40, 0x45]
r_array = [0x32, 0x3d, 0x3d, 0x45, 0x39, 0x43, 0x36, 0x36, 0x50]

combined = [chr(x) for x in q_array + r_array]

result = []
for c in combined:
    code = ord(c)
    if 0x21 <= code <= 0x7e:
        new_code = ((code - 0x21) + 0x2F) % 0x5E + 0x21
        result.append(chr(new_code))
    else:
        result.append(c)
final_string = ''.join(result)
print(final_string)
